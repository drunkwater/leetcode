bool isPalindrome(int x) {/* HuiWen number */
	/* sanity check */
	char str[64] = {0};

	int index = 0;
	while(0 != x)
	{
		str[index] = x%10;
		index++;
		x /= 10;
	}

	int  i = 0;
	for (i=0; i<index/2; i++)
	{
		if (str[i] != str[index -i -1])
		{
			return false;
		}
	}

	return true;
}


